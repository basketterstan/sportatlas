export const LANGUAGE_STORAGE_KEY = 'ha_language';
export const LANGUAGE_PICKED_KEY = 'ha_language_picked';

export const getAppLanguage = (profile?: UserProfile | null): AppLanguage => {
  if (profile?.language === 'es') return 'es';
  if (profile?.language === 'en') return 'en';
  const stored = localStorage.getItem(LANGUAGE_STORAGE_KEY);
  if (stored === 'es') return 'es';
  return 'en';
};

